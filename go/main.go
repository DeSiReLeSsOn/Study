package main

import "fmt"

func main() {
    var age int = 30 // Целое число
    var isStudent bool = false // Логическое значение
    var name string = "Иван" // Строка
    var price float64 = 19.99 // Число с плавающей точкой

    fmt.Println("Возраст:", age)
    fmt.Println("Студент:", isStudent)
    fmt.Println("Имя:", name)
    fmt.Println("Цена:", price)
}